package com.lec.java.for02;

public class For02Main {

	public static void main(String[] args) {
		System.out.println("For문 - 구구단 출력");
		
		System.out.println("구구단 2단");
		// 2 x 1 = 2
		// 2 x 2 = 4
		// ..
		// 2 x 9 = 18 
		
		int dan = 23;
		for(int i = 1; i <= 9; i++) {
			System.out.println(dan + " x " + i + " = " + (dan * i));
		}
		
		

	} // end main()

} // end class For02Main












